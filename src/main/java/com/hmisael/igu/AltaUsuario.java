    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        //Cargar roles en JComboBox
        List<Rol> listaRoles = controladora.traerRoles();
        if (listaRoles!=null){
            for (Rol rol: listaRoles){
                cbRoles.addItem(rol.getNombre());
            }
        }
        
        
    }//GEN-LAST:event_formWindowOpened

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String usuario = txtUsuario.getText();
        String contrasenia = txtContrasenia.getText();
        String rol = (String) cbRoles.getSelectedItem();
        
        controladora.crearUsuario(usuario, contrasenia, rol);
        mostrarMensaje("Usuario creado correctamente", "INFO", "Operación realizada");
        
    }//GEN-LAST:event_btnGuardarActionPerformed

     public void mostrarMensaje (String mensaje, String tipo, String titulo){
        JOptionPane optionPane = new JOptionPane(mensaje);
        if (tipo.equals("INFO")){
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }
        else if(tipo.equals("ERROR")){
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
    

